const pool = require("./database")

async function createWishlistTable() {
    const sql = `
    CREATE TABLE IF NOT EXISTS public.wishlist (
      wishlist_id INT GENERATED BY DEFAULT AS IDENTITY,
      account_id INT NOT NULL,
      inv_id INT NOT NULL,
      added_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
      CONSTRAINT wishlist_pk PRIMARY KEY (wishlist_id),
      CONSTRAINT fk_account FOREIGN KEY (account_id) REFERENCES public.account (account_id) ON DELETE CASCADE,
      CONSTRAINT fk_inventory FOREIGN KEY (inv_id) REFERENCES public.inventory (inv_id) ON DELETE CASCADE,
      CONSTRAINT unique_wishlist_item UNIQUE (account_id, inv_id)
    );
    `
    try {
        await pool.query(sql)
        console.log("Wishlist table created successfully.")
    } catch (error) {
        console.error("Error creating wishlist table:", error)
    }
}

createWishlistTable()
